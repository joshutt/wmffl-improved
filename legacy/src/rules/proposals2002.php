<H1 ALIGN=Center>Current Rule Proposals</H1>
<HR size = "1">

<P>This is the list of proposals voted on for the 2002 WMFFL season.
If you would like to suggest a rule proposal, you may
    do so on the <A HREF="proposesubmit.php">proposals page</A>. The part of
each proposal that appears in <I>Italics</I> is what effect this proposal will
have on the ruleset, if passed.</P>

<P><B>Proposal 2002.1a - Change Offensive Alignments</B><BR>
<FONT COLOR="red">Status: Rejected 0-6 with 2 No Votes</FONT><BR>
Change the current rule that requires an offense to run 2 RBs, 2 WRs and
1 TE (2-2-1), to only allow 1 RB.<BR>
<BLOCKQUOTE><I>Amend rule V.A.2 to read "1 QB, 1 RB, 2 WR, 1 TE, 1 K,
1 OL, 2 DL, 2 LB, 2 DB and 1 HC"</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.1b - Change Offensive Alignments</B><BR>
<FONT COLOR="red">Status: Rejected 2-5 with 3 Abstain</FONT><BR>
Change the current rule that requires an offense to run 2 RBs, 2 WRs and
1 TE (2-2-1), to also allow 3-1-1, 1-3-1 and 1-2-2.<BR>
<BLOCKQUOTE><I>Amend rule V.A.2 to read the following:
<BLOCKQUOTE>1. 1 QB, 1 RB, 1 WR, 1 TE, 1 K, 1 OL, 2 DL, 2 LB, 2 DB and 1 HC<BR>
&nbsp;&nbsp;&nbsp;a. In addition to 2 other offensive players (RB, WR or TE)<BR>
&nbsp;&nbsp;&nbsp;b. No more than 2 TEs may be activated</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.1c - Change Offensive Alignments</B><BR>
<FONT COLOR="red">Status: Rejected 3-4 with 3 Abstain</FONT><BR>
Same as b, but the 3-1-1 is not allowed.<BR>
<BLOCKQUOTE><I>Amend rule V.A.2 to read the following:
<BLOCKQUOTE>2. 1 QB, 1 RB, 2 WR, 1 TE, 1 K, 1 OL, 2 DL, 2 LB, 2 DB and 1 HC<BR>
&nbsp;&nbsp;&nbsp;a. In addition to 1 other offensive player (RB, WR or TE)<BR>
</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2002.1d - Change Offensive Alignments</B><BR>
<FONT COLOR="red">Status: Rejected 0-6 with 2 No Votes</FONT><BR>
Similar to b and c except that only 2-2-1 and 1-3-1 would be allowed.  A team
would also be required to keep the same alignment throughout the season or
face a point penalty for the weeks after they switch alignments.<BR>
<BLOCKQUOTE><I>Amend rule V.A.2 to read the following:
<BLOCKQUOTE>2. 1 QB, 1 RB, 2 WR, 1 TE, 1 K, 1 OL, 2 DL, 2 LB, 2 DB and 1 HC<BR>
&nbsp;&nbsp;&nbsp;a. In addition to 1 other player at either RB or WR<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. The extra position (RB or WR) must be selected by the team at the start of the season<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ii. If a team changes the alignment during the season, thier head coach will receive point penalties of 5 pts, 3 pts and 1 pt for each week following<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;iii. If a team switches back to a previous alignment before the penalty cycle is complete the head coach penalties will be 5 minus what the penalty would have been.  This penalty will decrease by 2 pts each week.
</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2002.2 - Change Defensive Alignments</B><BR>
<FONT COLOR="red">Status: Rejected 3-4 with 3 Abstain</FONT><BR>
Change the current rule that requires a defense to run 2 DLs, 2 LBs and
2 DBs (2-2-2), to also allow 3-1-2, 2-1-3, 1-3-2, 1-2-3.<BR>
<BLOCKQUOTE><I>Amend rule V.A.2 to read the following:
<BLOCKQUOTE>2. 1 QB, 2 RB, 2 WR, 1 TE, 1 K, 1 OL, 1 DL, 1 LB, 2 DB and 1 HC<BR>
&nbsp;&nbsp;&nbsp;a. In addition to 1 other defensive front player (DL or LB)<BR>
&nbsp;&nbsp;&nbsp;b. In addition to 1 other defensive player (DL, LB or DB)</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.3 - Point Penalty for Bye Players</B><BR>
<FONT COLOR="red">Status: Passed 7-2 with 1 Abstain</FONT><BR>
Add a 2 point penalty for activated players on byes, not filled or not
on NFL teams<BR>
<BLOCKQUOTE><I>Amend rule V.A.3 to read the following:
<BLOCKQUOTE>3. Submitting incomplete lineups can result in penalties and fines<BR>
&nbsp;&nbsp;&nbsp;a. Activating players on NFL bye weeks will result in a $1
fine and a 2 point penalty per violation<BR>
&nbsp;&nbsp;&nbsp;b. Activating players not on a NFL team will result in a $1
fine and a 2 point penalty per violation<BR>
&nbsp;&nbsp;&nbsp;b. Activating the incorrect number of players will result in a
$1 fine and a 2 point penalty per violation</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.4 - Change Automatic Drafting Method</B><BR>
<FONT COLOR="red">Status: Passed 5-2 with 3 Abstains</FONT><BR>
Make it so that when no method is specified for how a team's draft picks
are to be made the computer will not pick a third player from a position
until all of the positions have at least 2 players.<BR>
<BLOCKQUOTE><I>Add rule VIII.A.3.b reading "When the league picks players
for a team, it will select two players at every position, before selecting
a third player from any position."</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.5 - Get Rid of 1-0 Wins</B><BR>
<FONT COLOR="red">Status: Passed 6-2 with 2 Abstentions</FONT><BR>
Currently, when two teams are playing each other and the final scores are
both negative, the scores are reset to 0-0 and the team who had the most
total points gets an extra point and wins 1-0.  This proposal would eliminate
that case and the 0-0 tie would remain.<BR>
<BLOCKQUOTE><I>Repeal rule V.C.2.b</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.6 - Clean Up Article V</B><BR>
<FONT COLOR="red">Status: Passed 7-0 with 3 No Votes</FONT><BR>
Article V is an absolute mess.  This proposal would reorganize it, without
changings its meaning or interpretation.  It would also add the explict
statement that activations rollover from week to week.<BR>
<BLOCKQUOTE><I><A HREF="proposals20026detail.shtml">Details</A></I></BLOCKQUOTE></P>

<P><B>Proposal 2002.7 - Offensive Player Fumble Recovery Points</B><BR>
<FONT COLOR="red">Status: Rejected 2-6 with 2 No Votes</FONT><BR>
Allow offensive players to receive 2 points for fumble recoveries, just
as defensive players do.<BR>
<BLOCKQUOTE><I>Add rule V.B.1.c reading as follows:<BR>
<BLOCKQUOTE>c. Fumble Recovery<BR>
&nbsp;&nbsp;&nbsp;i. 2 pts</BLOCKQUOTE><BR>
Repeal rule V.B.7.c.iii</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.8 - Random Draft Order</B><BR>
<FONT COLOR="red">Status: Rejected 1-5 with 1 Abstain and 3 No Votes</FONT><BR>
Eliminate the weighted lottery for non-playoff teams and make all 6
non-playoff draft positions based on random draw.<BR>
<BLOCKQUOTE><I>Amend rule VI.D.3.b to read "The first six positions will
be determined by random draw of non-playoff teams."<BR>
Repeal rule VI.D.3.c</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.9 - Expand Roster Size</B><BR>
<FONT COLOR="Red">Status: Withdrawn</FONT><BR>
Expand the size of the roster to between 26 and 28 players.<BR>
<BLOCKQUOTE><I>Amend rule IV.A to change the number 25 to a number
between 26 and 28.</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.10 - Individual Kickers</B><BR>
<FONT COLOR="Red">Status: Passed 6-1 with 1 Abstain and 2 No Votes</FONT><BR>
Change kickers to individual kickers.  All current team kickers would
become the individual kicker that was the primary kicker of that team
at the conclusion of the 2001 season.<BR>
<BLOCKQUOTE><I>Amend rule IV.B.5 to read "Kickers"</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.11 - Kicker Penalties</B><BR>
<FONT COLOR="red">Status: Failed 2-6 with 2 No Votes</FONT><BR>
There is no excuse for a kicker missing an extra point.  Up the penalty
on missed extra points and field goals under 30 yards to 3.  Missed
field goals between 30-39 should have a -2 penalty and 40-49 should
have -1.  Missed field goals 50 and over would not recieve penalty.<BR>
<BLOCKQUOTE><I>Amend rule V.B.5.a.ii to read "Missed = -3 pts"<BR>
Amend rule V.B.5.b.v to read "Missed 0-30 yards = -3 pts"<BR>
Add rules V.B.5.b.vi and V.B.5.b.vii reading:
<BLOCKQUOTE>vi. Missed Field Goal 30-39 yards<BR>
&nbsp;&nbsp;&nbsp;a. -2 pts<BR>
vii. Missed Field Goal 40-49 yards<BR>
&nbsp;&nbsp;&nbsp;a. -1 pt</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2002.12 - Individual Head Coaches</B><BR>
<FONT COLOR="Red">Status: Passed 6-1 with 1 Abstain and 2 No Votes</FONT><BR>
Treat head coaches as individuals and not units.  Thus, when Tony Dungy
is fired in Tampa Bay, whoever had him doesn't get Jon Gruden but gets
Tony Dungy in Indianapolis.  If a team's coach is not coaching any team
they may select a new head coach as they would any other time.<BR>
<BLOCKQUOTE><I>Amend rule IV.B.10 to read "Head Coach"<BR>
Amend rule IX.H.3 to read "If a team wishes to fire their head coach
during the season, they may do so if that coach is not the current head
coach of an NFL team or if all of the following are true:"
</I></BLOCKQUOTE></P>

<P><B>Proposal 2002.13 - Kicker and Offensive Line Protections</B><BR>
<FONT COLOR="Red">Status: Passed 6-1 with 1 Abstain and 2 No Votes</FONT><BR>
Make the season modifier for kickers and offensive line progress at the
slower rate that defensive players do, instead of the faster rate of
offensive players.<BR>
<BLOCKQUOTE><I>Amend rule X.E.2.a to read "QB, RB, WR and TE"<BR>
Amend rule X.E.2.b to read "K, OL, DL, LB and DB"
</I></BLOCKQUOTE></P>

