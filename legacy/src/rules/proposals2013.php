<b>Proposal 2013.1a - Realignment, Simple</b><br/>
<b>Sponsor: Commissioner</b><br/>
<a class="ballot">Status: <font color="red">On Ballot</font></a><br/>
Swap one team from the division with the best record over the last three years, with one team from the division with the worst record over the last three years in a way that minimizes the difference in records.
<blockquote><i>No rule change required</i></blockquote>
</p>

