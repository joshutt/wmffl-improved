<p>
<b>Proposal 2013.1c - Realignment, Radical</b><br/>
<b>Sponsor: Commissioner</b><br/>
<a class="ballot">Status: <font color="red">Rejected, 3-8, 1 no vote </font></a><br/>
Return to two divisions by abolishing one division and putting the teams evenly into the other two divisions in a way that balances the three year record between the two divisions.  Doing this the schedule will be the first 11 weeks of the season every team will play every other team once.  Then the bottom two teams from each division will be eliminated and play a round-robin over the last three games, the Toilet Bowl would be eliminated.  The remaining four teams in each division would play each other over the last three weeks, with the best record in those games winning the division (tiebreak would be overall record).  Two wild card teams would also make the playoffs based on overall record.  All four playoff teams would be seeded 1-4 based on overall record, without regard to division titles.
<blockquote><i>TBD</i></blockquote>
</p>

<p>
<b>Proposal 2013.1d - Realignment, Relegation</b><br/>
<b>Sponsor: Commissioner</b><br/>
<a class="ballot">Status: <font color="red">Rejected, 1-6, 2 abstain, 3 no votes</font></a><br/>
Embrace that one division will always be better and switch to a relegation system.  The divisions are ranked Burgundy, Gold and White.  When a team wins the division in the Gold or White they are promoted to the next division up, teams that finish in last in Burgundy or Gold move down a division.  This would happen every year.
<blockquote><i>Add rule I.C:
<blockquote>Each division will be identified as first, second or third division<br/>
&nbsp;&nbsp;&nbsp;1. Division winners for each division other than the first division, will be promoted to the next division up the following season<br/>
&nbsp;&nbsp;&nbsp;2. The fourth place team in each division, other than the third division, will be relegated to the next division down the following season
</blockquote></i></blockquote>
</p>

