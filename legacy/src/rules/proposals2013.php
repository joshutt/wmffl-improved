<p>
<b>Proposal 2013.4 - Add 4 rounds to Draft</b><br/>
<b>Sponsor:  Werewolves</b><br/>
<a class="ballot">Status: <font color="red">Passed, 8-0, 1 abstain, 3 no votes</font></a><br/>
Extend the draft from 12 rounds to 16 rounds.  At the same time revamp the timing, so that instead of 2 minutes per pick, each team gets 2 minutes for the
whole draft, then 30 seconds added on when their pick comes around.  Left over time would remain until the next pick.  (eg, if a team had 1:45 left on their
clock, when their next pick came around it would increase by 30 seconds to 2:15.  If they then took 20 seconds they would have 1:55 left.  So the overall
clock could grow.)  Clock growth would be capped at 5 minutes.
<blockquote><i>In rule VI.D.1 change the word "three" to "four"<br/>
Replace rule VII.B to read:
<blockquote>B. Each team will have a draft clock that runs during their picks<br/>
&nbsp;&nbsp;1. Each team's clock will start at 2 minutes<br/>
&nbsp;&nbsp;2. When a team goes on the clock for each pick, 30 seconds will be added to their clock<br/>
&nbsp;&nbsp;3. If a team's clock reaches 0:00 their pick will be auto selected.<br/>
&nbsp;&nbsp;4. At no time may a team's clock be larger than 5 minutes
</blockquote>
</i></blockquote>
</p>

<p>
<b>Proposal 2013.5 - Luxury Tax/Handi-cap Protections</b><br/>
<b>Sponsor:  Woodland Rangers</b><br/>
<a class="ballot">Status: <font color="red">Rejected, 2-7, 3 No Votes</font></a><br/>
Having played in other FFLs, I found the uniqueness of the protections a bit refreshing from the all keeper leagues and the re-draft leagues. Having to choose each year who to keep or who let go becomes a very crucial process. It could make or break a team in come cases.  There are some coaches who may like to keep a certain player(s) for whatever reason, but can't due to being a point or two short, and no one willing or able to trade away their protection points.  Here is my proposal. A coach may protect X amount of players up to 10 max protection points over the protection point limit, but will incur a game handi-cap penalty of the amount of points equal to what the team is over.<br/>
Example:<br/>
Werewolves have used all 56 of their PP. They have 2 Defensive players they really want to keep Crayola Washington 4 Points, and Centurayon Jones 4 Points,  for a toal of 8 points. That 8 points over will now be 8 points for all of the Werewolves opponents for the entire season. Every opponent will be "spotted" 8 points before the ball is snapped.<br/>
 Dropping players will lower the cost of the penalty. So if the Werewolves drop Joseph Wladislaw 9 points, then the will be under the PP limit (55) and then not incur a penalty.<br/>
Season to taste, but just a general idea.
<blockquote><i>Add the following sub-rules to X.A: <br/>
1. A team may purchase up to 10 additional protection points.<br/>
2. For each protection point purchased the team will give a bonus of that much to their opponents each week during the regular season
</i></blockquote>
</p>

