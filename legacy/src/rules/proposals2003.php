<H1 ALIGN=Center>Current Rule Proposals</H1>
<HR size = "1">

<P>This is the list of proposals to be voted on for the 2003 WMFFL season.
If you would like to suggest a rule proposal, you may
    do so on the <A HREF="proposesubmit.php">proposals page</A>. The part of
each proposal that appears in <I>Italics</I> is what effect this proposal will
have on the ruleset, if passed.</P>

<P><B>Proposal 2003.1 - Change League Name</B><BR>
<B>Sponsor: Norsemen</B><BR>
<FONT COLOR="red">Status: Passed 6-1, 1 abstain, 2 No votes</FONT><BR>
Offically make the name of the league the "Washington Metropolitan Fantasy Football
League".  Change the offical colors to Burgandy and Gold.<BR>
<BLOCKQUOTE><I>No changes to the rules would be necessary</I></BLOCKQUOTE></P>

<P><B>Proposal 2003.2 - Adjust Late Payment Penalties</B><BR>
<B>Sponsor: Illuminati</B><BR>
<FONT COLOR="red">Status: Passed 6-2, 2 abstain</FONT><BR>
If a team is entitled to receive winningsfor their performance in a season and the
league does not pay them out, or offer to roll the winnings into the entry fee for
the following season, the team can not be penalized for late payments.<BR>
<BLOCKQUOTE><I>Add rule XI.A.2 to read the following:
<BLOCKQUOTE>Teams that have not been given the option to fully receive the previous
year's winnings will not be charged an a fine as a result of failing to pay the entry
fee.</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2003.3a - Failiure to Attend Draft</B><BR>
<B>Sponsor: Illuminati</B><BR>
<FONT COLOR="red">Status: Rejected 2-6, 1 abstain, 1 no vote</FONT><BR>
Failure to attend the draft will result in a $10 fine.<BR>
<BLOCKQUOTE><I>Add rule VIII.B to read the following:
<BLOCKQUOTE>Failure of a team to attend the draft will result in a $10 fine being
assessed against that team.
</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2003.3b - Failiure to Attend Draft 2</B><BR>
<B>Sponsor: Werewolves</B><BR>
<FONT COLOR="red">Status: Passed 7-3</FONT><BR>
A similar fine to version A would occur, but would only effect those teams that
do not provide a method for selecting players.<BR>
<BLOCKQUOTE><I>Add rule VIII.A.3.c to read the following:
<BLOCKQUOTE>Failure of a team to specify a picking method will also result in a $10 fine being
assessed against that team.
</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2003.4 - Toliet Bowl Draft Pick</B><BR>
<B>Sponsor: Werewolves</B><BR>
<FONT COLOR="red">Status: On Ballot</FONT><BR>
The Toliet Bowl loser can not be selected in the draft lottery.  They would receive 
whatever draft position they would have received if they had been in the lottery but
not been selected.  This would not effect the weight that each team receives.<BR>
<BLOCKQUOTE><I>Add rule VI.D.3.b.iii to read the following:
<BLOCKQUOTE>The loser of the previous year's Toliet Bowl will have a weight of 0.
</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2003.5 - Champion's Draft Pick</B><BR>
<B>Sponsor: Werewolves</B><BR>
<FONT COLOR="red">Status: Passed 6-0, 1 Abstain and 3 No Votes</FONT><BR>
The League Champion will pick last in the draft, regardless of previous year's record.
<BR>
<BLOCKQUOTE><I>Amend rule VI.D.3.a to read the following:
<BLOCKQUOTE>a. Playoff teams will draft in the last four draft positions<BR>
&nbsp;&nbsp;&nbsp;i. The league Champion will draft last<BR>
&nbsp;&nbsp;&nbsp;ii. The remaining playoff teams will pick in reverse order of regular season record</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2003.6 - Offensive Fumble Points</B><BR>
<B>Sponsor: Werewolves</B><BR>
<FONT COLOR="red">Status: Passed 5-2, 3 abstain</FONT><BR>
The 2 point penalty for offensive fumbles will only apply if it is a fumble lost.<BR>
<BLOCKQUOTE><I>Amend rules V.B.2.c.ii, V.B.3.d.i and V.B.4.d.i all to read as the 
following:
<BLOCKQUOTE>Fumbles Lost = -2 pts</BLOCKQUOTE>
</I></BLOCKQUOTE></P>

<P><B>Proposal 2003.7 - Restructuring of Article VIII</B><BR>
<B>Sponsor: Werewolves</B><BR>
<FONT COLOR="red">Status: Passed 7-0, 3 No Votes</FONT><BR>
Restructure Article VIII as Section E of Article VI.  No change to any wording would occur.<BR>
<BLOCKQUOTE><I>Move rules VIII.A to VI.E.</I></BLOCKQUOTE></P>

<P><B>Proposal 2003.8 - New Barbarians Owner</B><BR>
<FONT COLOR="red">Status: Passed 6-0, 3 No Votes</FONT><BR>
Ownership of Barbarians would be transfered from Keppy Plitt to Warren Allen.<BR>
<BLOCKQUOTE><I>No changes to the rules would be necessary</I></BLOCKQUOTE></P>

