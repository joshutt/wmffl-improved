<blockquote><i>Update rule III.E to read:
    <blockquote>E. The two last place teams in each division with the worse records will play in week 15 for the Toilet Bowl<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;1. The loser of the Toilet Bowl will have their team renamed by the League Champion<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;2. The team must keep their new name until one of the following occurs<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a. The team qualifies for the playoffs<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b. The team changes owners<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;c. The team loses the Toilet Bowl again and gets renamed by the Champion<br/>
    </blockquote>
</i></blockquote>
</p>

