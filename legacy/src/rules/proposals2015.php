<p>
<b>Proposal 2014.1 - Increase entry fee to $100</b><br/>
<b>Sponsor: Tom Marsh</b><br/>
<a class="ballot">Status: <font color="red">Failed - 5-6, 1 abstain</font></a><br/>
 I propose raising the yearly fee from $75 to $100.  There has been some discussion over the past couple years about lack of participation by some of the league members (not trading, responding to requests, etc) so I'm thinking maybe raising the cost might get people more involved?
<blockquote><i>Amend rule IX.A by changing "$75" to "$100"</i></blockquote>
</p>

<p>
<b>Proposal 2015.1 - Make the defense more flexible</b><br/>
<b>Sponsor: Warren Allen</b><br/>
<a class="ballot">Status: <font color="red">Failed - 5-6, 1 abstain</font></a><br/>
Require that one of the defensive line be a defensive tackle.  Then add a flex defensive player.  This would make the defensive line-up 2 DL (1 must be DT), 2 LB, 2 DB and 1 flex.
<blockquote><i>Amend rule V.A.2 to say: "An active roster includes 1 HC, 1 QB, 1 RB, 2 WR, 1 TE, 1 K, 1 OL, 2 DL (at least one being a DT), 2 LB, 2 DB, 1 additional player at either RB, WR or TE and 1 additional player at either DL, LB or DB."</i></blockquote>
</p>

<p>
<b>Proposal 2015.2 - League Champion Renames Toilet Bowl Loser</b><br/>
<b>Sponsor: Tom Marsh and Josh Utterback</b><br/>
<a class="ballot">Status: <font color="red">On Ballot</font></a><br/>
