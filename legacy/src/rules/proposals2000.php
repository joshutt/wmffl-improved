<H1 ALIGN=Center>Current Rule Proposals</H1>
<HR size = "1">

<P>This is the list of proposals voted on for the 2000 WMFFL season.
If you would like to suggest a rule proposal for next year, you may
    do so on the <A HREF="proposesubmit.php">proposals page</A>.</P>

<P><B>Proposal 2000.1 - Change Name of the League</B><BR>
<FONT COLOR="Red"><B>Rejected</B></FONT><BR>
This would cause the league to offically be renamed from
WMFFL to Washington Metropolitan Fantasy Football League.  Other
suggestions for new names are still open for discussion.</P>

<P><B>Proposal 2000.2 - Change Name of Championship Game</B><BR>
<FONT COLOR="Red"><B>Rejected</B></FONT><BR>
Rename the championship game from the Wolverine Bowl, to something
more appropriate.  Suggestions are appreciated.</P>

<P><B>Proposal 2000.3 - Injuried Reserve</B><BR>
<FONT COLOR="Red"><B>Rejected</B></FONT><BR>
Allocate a few spots on the roster for players that are injured.
These players would not count against the roster limit, but would
be inelgible to be activated.  Once placed on IR a player could not
be removed until the end of the season.  Each team's IR would be
limited to 3 players.</P>

<P><B>Proposal 2000.4 - Transaction Rules Change</B><BR>
<FONT COLOR="Red"><B>Passed</B></FONT><BR>
Change the transaction rules so that instead of a drop counting as a transaction
point, a pickup will.  In addition each team would receive 3 additional tranaction
points to be used in the preseason.</P>

<P><B>Proposal 2000.5 - Transaction Deadline</B><BR>
<FONT COLOR="Red"><B>Pick up and drops passed, trades rejected</B></FONT><BR>
Change the transaction deadline so that pickups may be done through the
11th week of the season.  Trades would be allowed until the team is eliminated
from the playoffs or until the end of the regular season, which ever comes first.
Finally, drops would be allowed at anytime.</P>

<P><B>Proposal 2000.6 - Protections</B><BR>
<FONT COLOR="Red"><B>Passed in modified form</B></FONT><BR>
The protection process would be changed so that one player may only be
protected by the same team for three consecutive years.  For every year
above three that a player is protected it will cost an additional protection
per additional year. (ie, a player protected for 5 years would cost the 1 normal
protection, plus 2 additional protections.)<BR>
<B>The rule that passed is as follows: a player may be protected for three years
at the current protection levels, after three years thier cost increases to two
protection points, every three years after that thier protection cost increases
by an additional protection (ie, a 7 year protected player costs 3 protections).
All current players protected for two or more years will be considered to have
two years of protections</B></P>

<P><B>Proposal 2000.7 - Realignment Process</B><BR>
<FONT COLOR="Red"><B>Withdrawn</B></FONT><BR>
Change the realignment requirements so that if possible, no division that
has previously existed, in whole or part, may be recreated.  A second proposal
would be to eliminate the rotating realignment.</P>

<P><B>Proposal 2000.8 - Expansion</B><BR>
<FONT COLOR="Red"><B>Passed</B></FONT><BR>
Expand to either 10 or 12 teams.</P>

<P><B>Proposal 2000.9 - Division Based Schedule</B><BR>
<FONT COLOR="Red"><B>Irrelevant</B></FONT><BR>
Revise the current schedule formula (play everyone twice) so that
every team plays the teams within thier division more frequently
than teams outside the division.  If we stay at 8 teams, this means
everyone in your division 3 times, outside once except for one team
that you play twice.  For 10 teams, this means everyone in your
division twice and outside once except for one team which you play
twice.  Finally, for 12 teams, everyone in your division twice and
everyone out of your division once.</P>

<P><B>Proposal 2000.10 - Missing Draft</B><BR>
<FONT COLOR="Red"><B>Missing draft equals two extra transactions</B></FONT><BR>
The are two proposals pertaining to missing the draft.  The first would
remove the extra protections clause for people who miss the draft.  The
second proposal would have any owner who misses the draft three consecutive
years or four out of five years would be automaticlly removed from the league.
</P>

<P><B>Proposal 2000.11 - Playoff Ties</B><BR>
<FONT COLOR="Red"><B>Passed</B></FONT><BR>
Change the playoff tie breaking procedures so that the first tie breaker is
the best bench player at each of the 6 individual positions is counted.  The
second tie-breaker would be the head coach scored.  The final tie-breaker is
the better seed wins (home-field).</P>

