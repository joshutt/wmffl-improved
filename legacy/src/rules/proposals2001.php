<H1 ALIGN=Center>Current Rule Proposals</H1>
<HR size = "1">

<P>This is the list of proposals voted on for the 2001 WMFFL season.
If you would like to suggest a rule proposal, you may
    do so on the <A HREF="proposesubmit.php">proposals page</A>. The part of
each proposal that appears in <I>Italics</I> is what effect this proposal will
have on the ruleset, if passed.</P>

<P><B>Proposal 2001.1 - Change QB Team Units to Individual Players</B><BR>
At the completion of the 2001 season but before the NFL Super Bowl, the position
of Team Quarterback will be dissovled and become individual quarterbacks.  For
each Team QB on a team's roster, they should pick one player from that NFL team,
that is identified as a QB, to be the player on the roster.  From then on
Quarterback will be treated as if they were individual players.
<BLOCKQUOTE><I>Amend rule IV.B.1 to read "Quarterbacks"</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.1a - Change QB Team Units to Individual Players</B><BR>
The quarterback position is an individual position, players are drafted and
activated by name, and not by team.  In addition, having a named quarterback
on your team also includes backup players on that team.
<BLOCKQUOTE><I>Not sure yet</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.2 - Pass Defended Points</B><BR>
Allow defensive players to recieve 1 point for every pass defense.
<BLOCKQUOTE><I>Add Rule V.A.4.g.xi that reads as follows:
<BLOCKQUOTE>Pass Defense<BR>
&nbsp;&nbsp;&nbsp;a. 1 pt</BLOCKQUOTE></I></BLOCKQUOTE></P>

<P><B>Proposal 2001.3 - Transaction Deadline</B><BR>
Change the transaction deadline so that trades may be performed at any time
and pickups may be made up until the activation deadline for the final week
of the regular season.
<BLOCKQUOTE><I>Repeal rule IX.E<BR>
Amend rule IX.F to read "No pickups are allowed after the regular season until
the draft"</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.3a - Transaction Deadline</B><BR>
Change the transaction deadline so that trades may be performed at any time
and pickups may be made up until the activation deadline for the Championship
Week.
<BLOCKQUOTE><I>Repeal rule IX.E<BR>
Amend rule IX.F to read "No pickups are allowed after the championship game
until the draft"</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.4 - Eliminate Realignment</B><BR>
Eliminate the automatic realignment every two years and leave the current
divisions.
<BLOCKQUOTE><I>Repeal rule I.C</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.4a - Eliminate Realignment</B><BR>
Change automatic realignment to every three years.
<BLOCKQUOTE><I>Amend rule I.C to read "Every three years the divisions will be
realigned"
</I></BLOCKQUOTE></P>

<P><B>Proposal 2001.5 - Draft Order</B><BR>
Change the method of determining draft order to the following.  Teams that made
the playoffs will draft in the last four positions in reverse order of finish.
All non-playoff teams will be in the lottery with their lottery weight being
the finishing place minus 4.  The lottery will be used to determine the first
three draft positions.  All remaining positions will be filled in with the
lottery teams not selected, in reverse order of finish.  The draft order will
remain the same for the entire draft.
<BLOCKQUOTE><I>Repeal ruled VI.D.2 and VI.D.4<BR>
Amend rule VI.D.3 to read the following:
<BLOCKQUOTE>The order is determined at least one week before the draft.<BR>
&nbsp;&nbsp;&nbsp;a. Playoff teams will draft in the last four draft positions
in reverse order of finish<BR>
&nbsp;&nbsp;&nbsp;b. The first three positions will be determined by random,
weighted draw of non-playoff teams<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. The fifth place team will recieve a weight
of 1.<BR>
&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ii. Every other team will be weighted in reverse
order of finish, recieving a weight of 1 more than the previous team.<BR>
&nbsp;&nbsp;&nbsp;c. Remaining positions will be filled by non-playoff teams not
selected in the lottery, in reverse order of finish.</BLOCKQUOTE>
</I></BLOCKQUOTE>
</P>

<P><B>Proposal 2001.6 - Draft Date</B><BR>
Move the default draft date to the Saturday that occurs two weeks prior to the
start of the NFL seasn.  Also, the draft can only be moved to a date that would
allow at least a one month notice to all owners of the change.
<BLOCKQUOTE><I>Amend Rule VI.B to read "This meeting will take place on the
Saturday two weeks prior to the weekend that starts the NFL season".<BR>
Rule VI.B.1 reamins unchanged.<BR>
Add rule VIII.A.1.b that reads "All owners must be made aware of the moved
draft date at least one month prior to the new date."</I></BLOCKQUOTE>
</P>

<P><B>Proposal 2001.7 - Protections</B><BR>
Get rid of the at-large protection.
<BLOCKQUOTE><I>Amend rule X.A to read "Between seasons each team may select one
player at each position, from thier previous year's roster, which they can
protect."<BR>
Repeal rule X.C</I></BLOCKQUOTE>
</P>

