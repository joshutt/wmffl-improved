<p>
<b>Proposal 2017.3 - Add Game Planning</b><br/>
<b>Sponsor: Josh Utterback</b><br/>
<span class="ballot">Status: <span class="status">Passed 6-1, 1 abstain, 2 No Votes</span></span><br/>
Make it so that teams can game plan for their opponents.  A team can name one player on their roster and one on their opponent, that they game plan for.
The player on their team that they have game planned around will earn double points.  The player on the other team that they have game planned around will
only earn hald of their points.  If both teams game plan for the same player then they cancel each other other.  All game planning must be done before
Sunday kickoffs and will be held secret until that time.
<blockquote><i>Add rule V.D to read:
    <blockquote>D. Every week each team may optionally name one player on their team and one player on their opponent's team that they will game plan for.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;1. The player on their team that is game planned for will have their score doubled.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;2. The player on their opponent's team that is game planned for will have their score halved, with fractions rounded up.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;3. If both teams game plan for the same player then there will be no impact on their score<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;4. If a player is game planned for and that player is not on the activation roster, then the game planning will have no impact<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;5. Game planned players must be named no later than the earlier of the kickoff of that player's game or 12:55 pm on Sunday of the week in question.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;6. Each team's game planned players will not be publically revealed until 1:00pm on Sunday.<br/>
    </blockquote>
</i></blockquote>
</p>

