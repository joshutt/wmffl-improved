<BR><A NAME="VI"><B><LI>Draft Order</B></A>
<OL TYPE="A">
<LI>The league will meet at least once a year.
<LI>This meeting will take place on the Saturday two weeks prior to the weekend that starts the NFL season
<OL TYPE="1">
	<LI>This date could be changed to a weekend day after the start of NFL training camp, but before the regular season.
</OL>
<LI>The meeting will consist of a discussion on new rules, followed by the players' draft.
<LI>The draft will be broken into four quarters.
<OL TYPE="1">
	<LI>Each quarter will consist of three rounds.
	<LI><I>Repealed</I>
	<LI>The order is determined at least one week before the draft.
	<OL TYPE="a">
		<LI>Playoff teams will draft in the last four draft positions.
        <OL TYPE="i">
            <LI>The League Champion will draft last.
            <LI>The remaining playoff teams will pick in reverse order of regular season record.
        </OL>
        <LI><i>Repealed</i>
        <LI>Remaining positions will be filled by non-playoff teams in reverse order of record.
	</OL>
</OL>
<LI>The exact date of the draft will be selected to maximize the number of people able to attend.
<OL TYPE="1">
	<LI>No later than June 1st each team may submit to the commissioner a list of no more than four dates, meeting the criteria laid out in rule VI.B, in which they will be unable to attend the draft.
    <LI>The commissioner shall within four days of the recipt of the lists determine and announce the draft date.
	<OL TYPE="a">
		<LI>The date in which the least number of teams said that they would be unable to attend shall be selected as draft day.
		<LI>In the event that two or more days are tied the following precidence shall be followed.
        <OL TYPE="i">
            <LI>The weekend two weeks prior to the weekend that starts the NFL draft will have top priority.
            <LI>Among other weekends, the closer to the start of the NFL season will receive higher priority.
            <LI>Saturdays will have priority over Sundays.
        </OL>
	</OL>
	<LI>The owner missing the draft may specify a method for determining their draft picks.
	<OL TYPE="a">
        <LI>Failure to specify a picking method will result in the league picking the teams players based on the points scored the previous year.</li>
        <LI>When the league picks players for a team, it will select two players at every position, before selecting a third player from any position.</li>
        <LI>Failure of a team to specify a picking method will also result in a $10 fine being assessed against that team.</li>
	</OL>
</OL>
</OL>

<BR><A NAME="VII"><B><LI>Drafting</B></A>
<OL TYPE="A">
<LI>During the main draft, in each round, every team drafts based on the selection order.
<LI>A team is limited to 2 minutes per pick.
<OL TYPE="1">
<LI>Teams will be notified when there are only 30 seconds remaining.
<LI>If a team exceeds their time limit, they lose their pick.
</OL>
<LI>Remaining roster positions may be filled, after the draft, through standard transaction method.
</OL>

