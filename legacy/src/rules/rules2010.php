<BR><A NAME="IX"><B><LI>Transactions</B></A>
<OL TYPE="A">
<LI>Once the draft is over the rosters are set.
<LI>If a team wishes to drop a player, they may do so at no cost.
<LI>If a roster position is open, teams can add a player to their team by spending a transaction point.
<OL TYPE="1">
	<LI>Each team will recieve a number of transaction points equal to thier unused protection points.
	<LI>After the completion of the draft every team will receive an additional 5 transaction points.</li>
	<LI>Once a team is out of transaction points, they may purchase more at the cost of $1.
</OL>
<LI>Trades are allowed and anything maybe traded, subject to the roster restrictions.

<LI>Between the time activations are due for a given week and 1:00pm Eastern Time on the following Tuesday players may only be picked up via the waiver wire.
  <ol type="1">
  <li>All teams should list any players they would like to select off of waivers and order them in the priority they would like to receive them.</li>
  <li>At 1:00pm Eastern Time on Tuesday the waiver requests will be resolved.</li>
     <ol type="a">
     <li>Each team's waiver requests will be considered in the current waiver order.</li>
     <li>When a team is considered they will recieve as a pickup the highest priority player that is available.</li>
        <ol type="i">
        <li>Once a team picks up a player via waivers they are moved to the bottom of the waiver list and the next team in order is considered.</li>
        <li>If a team has no requested players available or did not request any players, then they remain at thier location in the waiver order and the next team is considered.</li>
        </ol>
    <li>Waiver consideration proceeds until there are no remaining waiver requests</li>
    </ol>
  <li>Once waiver requests are resolved, players may be picked up freely.</li>
    <ol type="a">
    <li>Any player dropped by a team in a given week will only be available through the waiver wire for that week.</li>
    <li>The team dropping a player may not pick that player back up until the completion of the next waiver wire.</li>
    </ol>
  <li>Any team that has a number of players exceeding the total allowable roster limit, at the time activations are due for a given week, will be required to drop all players picked up that week.</li>
  </ol>

<LI>No pickups are allowed after the Championship Game until the draft.
<LI>Players may be cut from a team at any time.
<LI>Head coaches are exempt from the above stated rules, but must adhere to the following.
<OL TYPE="1">
	<LI>Head Coaches are automatically retained at the end of the season.
	<LI>If a team so chooses, a coach can be fired in between seasons.
	<LI>If a team wishes to fire their head coach during the season, they may do so if that coach is not the current head coach of an NFL team or if all of the following are true:
	<OL TYPE="a">
		<LI>The NFL team that the coach represents must have a losing record.
		<LI>The WMFFL team that has the coach must also have a losing record.
		<LI>If these conditions are not met, a coach cannot be fired.
		<LI>The firing of a coach does not use a transaction point.
	</OL>
</OL>
</OL>

