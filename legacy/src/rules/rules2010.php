<BR><A NAME="XI"><B><LI>Financial Situations</B></A>
<OL TYPE="A">
<LI>At the beginning of the season each team will pay the league an entrance fee of $75.
<OL TYPE="1">
    <LI>Failure to pay the entry fee by the date of the draft will result in a series of fines.</li>
<OL TYPE="a">
<LI>The day following the draft a $1 fine is levied.
<LI>Two days following the draft a $5 fine is placed.
<LI>Every 30 days thereafter (until the team's balance is positive) will result in a $5 fine.
<LI>If a team has a negative balance after week 14, they will be inelgible for the playoffs.
</OL>
<LI>Teams that have not been given the option to fully receive the previous year's winnings will not be charged an a fine as a result of failing to pay the entry fee.</li>
</OL>
<LI>The entrance fees, plus any fees that were accumulated throughout the season, are added to a pot.
<LI>The pot is divided into three parts.
<OL TYPE="1">
<LI>The first part is for league finances.
<OL TYPE="a">
<LI>This is money that the league uses for photocopies, software, mail, etc.
<LI>The amount that goes to league finances is either the expected expenditures plus 10% or 25% of the total pot, whichever is less.
<LI>The remaining money is returned to teams in the form of winning payments.
</OL>
<LI>One third of the remaining money goes to weekly winnings.
<OL TYPE="a">
<LI>The weekly winnings are divided by the total number of regular season games (currently 70).
<LI>This is the amount that each team will receive for each regular season win, ties count as half a win.
</OL>
<LI>Two-thirds goes to end of the year winnings.
<OL TYPE="a">
<LI>50% goes to the League Champion
<LI>25% goes to the runner-up
<LI>10% goes to each of the Semi-Final Losers
<LI>2.5% goes to each Division Champion
</OL></OL></OL>

