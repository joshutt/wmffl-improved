<p><b>Proposal 2009.1 - Tie-Breakers</b><br/>
<b>Sponsor: Werewolves</b><br/>
<a class="ballot">Status: <font color="red">Passed, 6-2, 2 abstain</font></a><br/>
Changing tie-breaking rules to include strength of victory, while removing point differential, record in last 5 games and record vs .500.  Also, clarification of some other existing tie-breaking rules.
<blockquote><i>Update rule II.E.1.d to read:
        <blockquote>Combined record of all teams defeated (Strength of Victory) </blockquote>
        Move rule II.E.1.i to II.E.1.g and reword:
        <blockquote>Longest playoff drought</blockquote>
        Remove rule II.E.1.h<br/>
        Add rule II.E.3:
        <blockquote>All ties within a division must be broken before breaking ties across divisions.Teams always retain thier position within the division.</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2009.2a - Transaction Deadline Changes (Allow Free agents)</b><br/>
<b>Sponsor: Sacks on the Beach</b><br/>
<a class="ballot">Status: <font color="red">Failed 3-5, 2 abstain</font></a><br/>
Update the transaction deadline rule to allow teams in the playoffs to pickup free agents during the playoffs.
<blockquote><i>Update rule V.A.5 to read:
        <blockquote>Teams may only activate players in the playoffs that were on their roster for the final game of the regular season, except for players that were free agents during the final game of the regular season.</blockquote>
        Add rule V.A.6 reading:
        <blockquote>Rosters for all teams not in the playoffs are locked from the final game of the regular season until the end of the Championship Game, and those losing in the playoffs locked until after the Championship game.</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2009.2b - Transaction Deadline Changes (Restricted Free Agents)</b><br/>
<b>Sponsor: Crusaders</b><br/>
<a class="ballot">Status: <font color="red">Failed 2-5, 1 abstain, 2 no votes</font></a><br/>
Same as above, except that the only players available to be picked up are ones that never appeared on a roster.
<blockquote><i>Update rule V.A.5 to read:
        <blockquote>Teams may only activate players in the playoffs that were on their roster for the final game of the regular season, except for players that were never on a fantasy team during the current season.</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2009.3 - Free Market Protection Costs</b><br/>
<b>Sponsor: Werewolves</b><br/>
<a class="ballot">Status: <font color="red">On Ballot</font></a><br/>
The protection cost of a position will be based on a free market system.  If the number of players protected at a position in the previous 4 years is too high, then the cost of that position increases, and vice versa.  A position is considered to be too high or too low if the number protected is outside the range of +/- 5 from the target.  The four year range is reset when the cost changes.  The targets will be 1 player per team per position per year, except for RB which will be 1.5, WR which will be 1.25 and LB which will be 1.25.
<blockquote><i>Add rule X.F reading:
    <blockquote>The target number of protections for each position each year shall be equal to their target number times the number of teams in the league.<br/>
    &nbsp;&nbsp;1. Target numbers<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;a. RB<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. 1.5<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;b. WR and LB<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. 1.25<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;c. All other positions<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. 1<br/>
    &nbsp;&nbsp;2. The number of protections at each position in the shorter of the previous 4 years or years since the last position cost change for the position shall be compared to the target number of protections over that same time frame.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;a. If the number of actual protections exceeds the targeted protections by more than 5, then the appropriate position in rule X.E.1 shall have their point value increased by 1.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;b. If the targeted number of protections exceeds the actual number of protections by more than 5, then the appropriate position in rule X.E.1 shall have their point value decreased by 1.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. However no position shall have it's value decreased below 1.<br/>
    &nbsp;&nbsp;3. The calculation for a given season shall be determined after protections are due and announced at the draft.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;a. The changes will take effect the following season.</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2009.4 - Remove Draft Lottery</b><br/>
