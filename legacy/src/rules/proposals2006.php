<p><b>Proposal 2006.1 - Trade Deadline</b><br/>
<b>Sponsor: MeggaMen</b><br/>
Impose a trade deadline after week 10 of the regular season.  Waiver and free agency pick-ups
would continue to be ok.<br/>
<blockquote><i>Add rule IX.D.1 to read the following:
        <blockquote>No trade may occur after activations are due in week 10 of the regular season, until after the NFL Super Bowl.</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2006.2 - Limit to 5 RBs on Roster</b><br/>
<b>Sponsor: Lindbergh Baby Casserole</b><br/>
Every team is limited to 5 RBs on their roster at any one time<br/>
<blockquote><i>Add rule IV.B.2.a reading the following:
        <blockquote>No team may ever have more than five Running Backs on their roster at any given time.
        </blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2006.3 - Revise Method for Determine Draft Day</b><br/>
Change method for determing draft date to something less restrictive.  The proposal simply selects
the date which the most teams can attend.  Teams are limited to selecting on 4 days they can't
make it.<br/>
<blockquote><i>Change rule VI.E to read as follows:
        <blockquote>The exact date of the draft will be selected to maximize the number of people able to attend.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;1. No later than June 1st each team may submit to the commissioner a list of no more than four dates, meeting the criteria laid out in rule VI.B, in which they will be unable to attend the draft.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;2. The commissioner shall within four days of the recipt of the lists determine and announce the draft date.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;a. The date in which the least number of teams said that they would be unable to attend shall be selected as draft day.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;b. In the event that two or more days are tied the following precidence shall be followed.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;i. The weekend two weeks prior to the weekend that starts the NFL draft will have top priority.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;ii. Among other weekends, the closer to the start of the NFL season will receive higher priority.<br/>
            &nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;&nbsp;iii. Saturdays will have priority over Sundays.
        </blockquote>
        Current rule VI.E.3 should be attached to the end of this new rule.<br/>
        Change the word "sometime" in rule VI.B.1 to "a weekend day"
</i></blockquote>
</p>

