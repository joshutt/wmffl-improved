<p><b>Proposal 2006.5 - Roll Over Transaction Points</b><br/>
<b>Sponsor: Lindbergh Baby Casserole</b><br/>
Any transaction points left over from the previous season will increase the following season's 
protection points.  Any transaction points over the limit will reduce the following season's 
protection points by 5 pts for every 10 extra transactions.<br/>
<blockquote><i>Append the following rules to rule X.A<br/>
        <blockquote>1. Any transaction points remaining from the previous season will be added to this total.<br/>
            2. For every 10 extra transactions purchased the previous season, the protection point total will be reduced by 5.
        </blockquote>
</i></blockquote>
</p>


