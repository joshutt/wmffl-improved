    </blockquote>
</i></blockquote>
</p>


<p>
<b>Proposal 2016.1 - Clarify Official Game Scores</b><br/>
<b>Sponsor: Josh Utterback</b><br/>
<a class="ballot">Status: <font color="red">Passed, 8-0, 3 no votes</font></a><br/>
Formalize the unwritten policy around stat changes that occur after games are official. 
<blockquote><i>Add rule V.A.6 to read:
    <blockquote>6. Player's scores will be considered official at 8am eastern time on the Tuesday morning immediately following the NFL games.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;a. If the NFL makes an official scoring change following that time, the league will not update the scores unless any owner provides notification to the Commissioner of the scoring change prior to the following Saturday at noon eastern time.<br/>
    &nbsp;&nbsp;&nbsp;&nbsp;b. If the scoring change affects week 14 scores or the playoffs, the notification of change must be provided no later than one hour prior to the start of the first NFL game of the following week.<br/>
    </blockquote>
</i></blockquote>
</p>


<p>
