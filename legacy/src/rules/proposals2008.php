<h1 align=center>Current Rule Proposals</h1>
<hr SIZE = "1"/>

<p>This is the list of proposals to be voted on for the 2008 WMFFL season.
If you would like to suggest a rule proposal, you may
    do so on the <a href="proposesubmit.php">proposals page</a>. The part of
each proposal that appears in <i>Italics</i> is what effect this proposal will
have on the ruleset, if passed.</p>


<p><b>Proposal 2007.2b - Flex Position</b><br/>
<b>Sponsor: Crusaders</b><br/>
<a class="ballot">Status: Passed, 7-3</a><br/>
Change one RB activation slot to a flex position that can be used by either a RB, a WR or a TE.<br/>
<blockquote><i>Update rule V.A.2 to read:
        <blockquote>An active roster includes 1 HC, 1 QB, 1 RB, 2 WR, 1 TE, 1 K, 1 OL, 2 DL, 2 LB, 2 DB and 1 additional player at either RB, WR or TE.
        </blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2008.1 - Allow Activations Past Thursday for Early Games</b><br/>
<b>Sponsor: Sacks on the Beach</b><br/>
<a class="ballot">Status: Passed, 8-2</a><br/>
On weeks when the first games take place prior to Sunday, allow activations for players that haven't played yet until Sunday.
<blockquote><i>Update rule V.A.1 and sub-rules to read:
        <blockquote>Any player listed as active 30 minutes prior to kickoff of their team's NFL game will be considered an active player for that week.<br/>
        &nbsp;&nbsp;&nbsp;&nbsp;a. The activation status of a player may not be changed after that deadline.<br/>
        &nbsp;&nbsp;&nbsp;&nbsp;b. A player's activation status will not change from the previous week, unless explicitly done so by the owner.
        </blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2008.2 - Protection Points for Players Protected 9 or more years</b><br/>
<b>Sponsor: Werewolves</b><br/>
<a class="ballot">Status: Failed, 4-6</a><br/>
Every year a player is protected beyond eight years will cost an additional protection modifier point.
<blockquote><i>Update rule X.E.2.a.vii and X.E.2.b.v to each read:
        <blockquote>8 Years</blockquote>
Add rules X.E.2.a.viii and X.E.2.b.vi each reading:
        <blockquote>Each additional year<br/>
        &nbsp;&nbsp;&nbsp;&nbsp;a. +1 per year</blockquote>
</i></blockquote>
</p>

<p><b>Proposal 2008.3 - Transaction Deadline</b><br/>
<b>Sponsor: Werewolves</b><br/>
<a class="ballot">Status: Passed, 7-3</a><br/>
Players aquired after activations are due in week 14 may not be activated in the playoffs.
<blockquote><i>Add rule V.A.5 reading:
        <blockquote>No player may be activated for a team in the postseason, unless that player was on that team's roster at the point activations were due for the final week of the regular season.</blockquote>
</i></blockquote>
</p>

