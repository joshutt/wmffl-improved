system incorporates three
factors; the order of finish, last year's deviation and the number of losses.  Below
you will find each team's possible weight for each of these three factors, as well
as the total range.  Remember if a team has a final weight of 0 or negative they
will automaticlly be placed in the final draft position.  These tables indicate each
team's draft weight for the 2001 draft.</P>

