WMFFL Newsletter Week #2
Home Page: http://wmffl.com
=======================================================
The Norsemen once again have an illegal lineup and will be fined $1.
The Crusaders, Hempaholics and Green Wave all activated players on
byes and are also fined $1.

Warren still owes his entry fee plus some.  For a total of $60.30.
So everyone point at and mock Warren.

As far as the request from the War Eagles to cause the Norsemen to
forfeit this week.  That claim has been rejected on the grounds that
league custom and the overwelming precedent is to allow activations
to carry over week to week.  However, there is some precendent to
removing damagingly inactive players from the league mid-season.  I
hope it doesn't come to this but be aware that that is a possiblity.
BYRON: Submit a valid lineup and this whole conversation will be
moot!!!!!  PLEASE!!!!

Finally, as I sent out earlier, if you are using the league software
you MUST upgrade to version D17.  If you don't you will get lots of
errant information.  The reason for this is the cancellation and
reschedule of week 2 in the NFL.

