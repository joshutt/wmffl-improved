WMFFL Newsletter Week #14
Home Page: http://wmffl.com
=======================================================
Norsemen activated a bye week player.

The Werewolves clinched the final playoff spot.  The first round of 
the playoffs will feature the Freezer Burn vs the Werewolves and the
Crusaders vs the Hempaholics.  The Toliet Bowl will be a rematch of 
Championships V and VIII, Norsemen vs ZEN.  For those teams still 
playing, there are some Saturday afternoon games this week.  
Activations are due at 1:00 on Saturday.

Don't forget to send me your nominations for the three awards; Best 
Ownership Move, Bonehead Award and Owner of the Year.

