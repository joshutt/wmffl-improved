<H1 ALIGN=Center>New 2001 Awards</H1>
<H5 ALIGN=Center><I>December 17, 2001</I></H5>
<HR size = "1">

<P>
 After careful consideration, I have decided to do the end of the year awards a little differently this year. We will continue to have the raw points awards (top QB unit, WR units, Defense, Best GM etc), in addition I will bring back the individual MVP and best Defense (or offense if MVP is a defensive player) but will calculate those awards off of how well the players did vs other players at thier position. Not off of total points score, like it use to be done. Finally, there will be three awards that are voted on by the league. These awards will be <B>Best Ownership Move</B>, <B>Bonehead Move</B> and <B>Owner of the Year</B>.
</P>

<P>
 The <B>Best Ownership Move</B> should be for a single action (or directly related set of actions) that helped thier team out in a sizable and significant way. This action could be a trade, dropping someone, activating someone or anything else that you can think of that might have helped a team out. 
</P>

<P>
The <B>Bonehead Move</B> goes to a single team that did something really stupid that causes hinderence to that owner's own team. </P>

<P>
 Finally, the <B>Owner of the Year</B> should go to the team that had the best overall ownership. When considering this award, you should take all factors into account (what the team was working with, how successfull they were, how good thier transactions were, etc).</P>

<P> For each of these three categories there will be three top nominees. I will be accepting nominations until Sunday and will send out a ballot early next week. When you send me your nomination, make sure that you include who you are nominating and why you think they should get that award.</P>

