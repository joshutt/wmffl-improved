WMFFL Newsletter Week #9
Home Page: http://wmffl.com
=======================================================
There were no activation violations this week.

A number of player records were broken this week.

- The St. Louis Offensive Line scored 33 points, breaking
the single game record by Offensive Lines.  The old record
of 29 was set by San Francisco, during the playoffs of 1998.

- Michael Strahan has broken the single season points mark
by Defensive Linemen with 92.  The old record was 90, set
by Terry Wooden during the 1995 season.


