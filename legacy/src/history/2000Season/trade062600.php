<H1 ALIGN=Center>Trade Details</H1>
<H5 ALIGN=Center><I>June 26, 2000</I></H5>
<HR size = "1">

<P>This afternoon the Crusaders and Norsemen completed a blockbuster
trade that clearly ranks as the largest in league history.  The trade
involves three players, 6 draft picks, a protection and a couple different
conditional clauses.</P>

<P>The basis of the deal has the Crusaders sending the Indianapolis QB,
Jevon Kearse (DL-TEN) and a 5th round pick in 2000 to the Norsemen in
exchange for a free protection, Terrell Davis (RB-DEN) and 5th round picks
in 2000, 2001, 2002, 2003 and 2004.  In addition, if any of the three
named players (Indy QB, Kearse and Davis) are traded within the next
three years the other party receives a 3rd round pick.  Finally, this trade
will be nullified if Kearse, Davis or Peyton Manning announce, before
the protection deadline, that they will not play all or part of the 2000
season.</P>

<P>By far, this trade has more complexity and diversity than any other
trade this league has ever seen.  As Jon Hall, owner of the Crusaders,
commented "We're making history."  Exactly, what the historical result of
this trade are, will be seen in time.
</P>

