WMFFL Newsletter Week #7
Home Page: http://wmffl.com
=======================================================
With this week's win, the Crusaders now have 70 regular
season wins in franchise history.  Their next win will
be their 75th overall win.  Next in line are the
Norsemen with 68 wins in the regular season and 72
overall.  The Hempaholics are looking for win number 60
overall.

This week's tie between the Norsemen and Hempaholics is
the 5th in league history, not counting the
Championship last year, which went to tie breakers.

Remember that this week there is a Thursday game.  That
means that activations are due by 7:30 on Thursday.
Thankfully, the next Thursday game won't be until the
Thanksgiving.

The Barbarians and ZEN were both fined $1 for
activating players on bye.  ZEN also recieved a $5
charge because they had not paid thier entry fee as of
the 14th.


