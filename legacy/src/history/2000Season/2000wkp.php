WMFFL Newsletter Playoffs
Home Page: http://wmffl.com
=======================================================
The Freezer Burn win the Toliet Bowl.  That means that the Barbarians
will have bring the beer to next year's draft.

The Crusaders edged out the Hempaholics in a strong defensive game,
to advance to their fourth Championship game and third in four years.

The Norsemen kept their chance of repeating alive, by defeating the
MeggaMen.


