Championship Game
==========================================
#2 Crusaders (9-6) at #1 Norsemen (12-2-1)
  Norsemen beat Crusaders in week 14, 35-0
  Crusaders lead all-time series 12-8.
  But since 1993, series is 8-8, including 3-3 in last 6.
  In 1998 playoffs, Crusaders beat Norsemen 37-16
  Norsemen and Crusaders have best two all-time records, 79-50-1 .612
     and 77-54-1 .587, respectivly
  Norsemen have a 3 game playoff winning streak, tied for second
     longest ever
  Crusaders have won 2 championships (1992, 1997) and been to 3 (also
     1998)
  Norsemen have won 2 championships (1996, 1999)
  At least one of these teams has been in the Championship since 1996.
  Norsemen are favored by 6.5




