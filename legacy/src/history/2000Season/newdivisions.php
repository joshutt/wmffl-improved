<P>According to the WMFFL rules we must realign the divisions every two years.
The division champions since the last realignment, will remain in the division
they are in and all other teams will be considered for movement.  The new divisions
will be based on a win/loss differential over two years.</P>

<P>Because the Werewolves won the Blue Division in 1998 and the Norsemen won in 1999,
both of those teams will remain in the Blue Division.  The Crusaders won the Orange
Division in both 1998 and 1999.  The other 5 teams could be moved into either divison.
Below is a list of each team's won/loss differential over the past two years.</P>

