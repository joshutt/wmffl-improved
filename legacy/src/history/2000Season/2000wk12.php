Playoffs Watch
============
Blue Division
  Norsemen clinched division and #1 seed

Orange Division
  Crusaders win division with a win or MeggaMen loss
  MeggaMen win divsion if they win out and Crusaders lose out

Playoffs
  Norsemen have clinched division title and #1 seed
  Hempaholics clinch playoff berth with win or tie or Werewolves
        loss or tie
  Crusaders clinch playoff berth with win or Werewolves loss
  MeggaMen clinch playoff berth with win or Werewolves loss and
        Green Wave loss or tie
  Werewolves clinch playoff berth if they win out and MeggaMen
        lose out
  Green Wave clinch playoff berth if they win out and MeggaMen
        and Werewolves lose out
  ZEN clinch playoff berth if they win out and MeggaMen and
        Werewolves lose out and Green Wave lose or tie

Toliet Bowl
  Freezer Burn clinch Toliet Bowl berth with loss and Archers Who
        Say Ni win
  Archers Who Say Ni clinch Toliet Bowl berth with loss to Freezer
        Burn and loss in week 14.
  Barbarians clinch Toliet Bowl berth with loss or Green Wave and
        ZEN win
  ZEN clinch Toliet Bowl berth if they lose out and Barbarians win
        out
  Green Wave clinch Toliet Bowl berth if they lose out and
        Barbarians win out and ZEN win or tie.


