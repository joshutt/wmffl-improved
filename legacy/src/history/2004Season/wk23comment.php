<h4>This week</h4>
<p>Illuminati face the Gallic Warriors with a 3 game losing streak.  The Iluminati have only lost 4 in a row two times ever.  The Illuminati franchise has only ever lost to the Gallic Warriors twice, in 19 meetings!!</p>

<p>Norsemen vs Werewolves will be playing for the 28th time.  Norsemen lead all-time 18-9.  The all-time regular season record of the Norsemen is 93-72-2, of the Werewolves 92-72-3.</p>

<p>Last season's division champions (the Bug Stompers and Crusaders) meet.  The Bug Stompers have only defeated the Crusaders twice since 1995.  The Sacks on the Beach play the Whiskey Tango and the MeggaMen will meet the Rednecks, in an important division rivalry game.</p>

