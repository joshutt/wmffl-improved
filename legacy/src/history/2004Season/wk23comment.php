<h4>Introduction</h4>
<p>So this is a new segment dedicated to commentary on the season so far and looking forward.  Yes, it is a bit self-indulgent, why do I think that you care what I think about anything?  Well, you are reading this after all.  I'm not going to promise that I'll have a column every week, in fact I can promise that I won't have a column every week, but I'll do one whenever the urge compels me.  If at any point someone else wishes to provide a similar commentary feel free, I'll add it to the site.  Anyway, enough of that.</p>

<h4>Last Week</h4>
<p>First a quick review of last week.  The Bug Stompers lost to the Rednecks ending two long streaks.  The Bug Stompers end a 7 game winning streak, the longest in franchise history.  Their previous record of 5 was set weeks 1 through 5 of 1992!!!  Those are the first 5 weeks of league history.  It was all downhill from there.  If you only look at the regular season, 5 is still their record.</p>

<p>At the same time the Rednecks ended an 8 game non-winning streak dating back to week 8 of last season, tied for the 4th longest such streak.  The only bright spot in that streak was a 0-0 tie to Whiskey Tango, in week 13, when they were actually outscored.</p>

<p>The Werewolves shutout the Illuminati to start 2-0 for the first time since 1998.  The Gallic Warriors, MeggaMen and Whiskey Tango all win to move to 1-1.</p>

<h4>Player of the Week</h4>
<p>I'd give the player of the week award to Thomas Jones (RB-CHI) of the Rednecks.  Not because he scored the most points, he didn't, but because his points relative to his opposition were very good and he was clearly the difference in that game.</p>

<p>Week 1 I would have given to Donovan McNabb (QB-PHI) of the Bug Stompers.</p>


