WMFFL Newsletter Week #3
=======================================================
The Rednecks scored 121 offensive points, breaking the 119 record set
by the Norsemen in last year's Toliet Bowl.  The regular season record
was 118, set by the Norsemen in week 3 last year.  The offensive 
assault included 37 points by Jevon Walker (WR-GB), the 5th best WR 
performance ever.  As a result, the Rednecks defeat the MeggaMen, 
90-14.

The Werewolves moved to 3-0 with a 53-36 victory over the Norsemen.  
Illuminati picked up their first win of the season, 41-12 over the 
Gallic Warriors.  The Whiskey Tango and the Bug Stompers each move to 
2-1 with victories over the Sacks on the Beach and the Crusaders.


