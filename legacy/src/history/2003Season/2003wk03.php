WMFFL Scores for 2003 Week #3
=============================
This week the Green Wave activated a Philadelphia player.  Activating a
player on a bye incurs a two point penalty and $1 fine.

The MeggaMen and Crusaders faced down in a defensive battle, ending in 
a 0-0 tie, only the second time that has ever happened.  (The first 
time was Rednecks-Freezer Burn in week 1).  The Illuminati defeated the
Norsemen, to even the all-time series between those two at 10-10-1.  
This also extends the Norsemen's franchise record tying losing streak 
to 7 games.  This streak is tied with 6 others for the 4th longest in 
league history.  The Rednecks edged the Green Wave 15-11.  This gives 
the Rednecks a franchise high non-losing streak of 6 games.  It also 
sets a Green Wave franchise record with 8 straight loses, 3rd longest 
losing streak in league history.  The War Eagles defeated the Whiskey 
Tango to remain undefeated, sending Whiskey Tango to their first loss. 
Finally, the Freezer Burn picked up their first points for of the 
season by shutting out the Werewolves.

                                    
