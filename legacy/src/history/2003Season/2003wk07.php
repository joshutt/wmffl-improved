WMFFL Scores for 2003 Week #7
=============================
Whiskey Tango, Crusaders and Illuminati all activate players on byes 
and receive a $1 fine and a 2 point penalty.

With a loss this week the Green Wave have tied the 1993-94 Archers 
for the longest losing streak in league history at 12 games.  It is 
also the 7th time they have been shutout during the streak.  The 
Rednecks remain undefeated with a 2-0 victory over the Norsemen.  In 
the seven games this season only one has been decided by more than 4 
points.  The Freezer Burn play themselves back to .500 with a 48-0 
win over the Crusaders.  The MeggaMen defeat the Whiskey Tango for 
the fourth straight time and third straight shutout.  The War Eagles 
remained undefeated but had thier lead in the Burgandy division slip 
to one game,with a tie vs the Illuminati.  The War Eagles' unbeaten 
streak in now 7 games.

                                    
