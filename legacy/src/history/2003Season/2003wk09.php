WMFFL Scores for 2003 Week #9
=============================
The Whiskey Tango activated two bye players which costs them $2 and 4 
points.

The War Eagles remained undefeated, extending their unbeaten streak to
9 games, the fourth longest in league history.  It is also only the 
second time they have beaten the Werewolves in their last 11 meetings.
The Green Wave's record winless streak gets extended to 14 games with 
a loss to the Crusaders.  With this loss the Green Wave are eliminated
from playoff contention.

The Illuminati even the all-time regular season series vs the Whiskey 
Tango with a 12-7 victory.  The Rednecks fall to their second straight
defeat, losing 55-40 to the MeggaMen.  And the Norsemen helped 
themselves out, winning 41-28 over the Freezer Burn.

                                  
