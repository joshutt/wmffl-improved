WMFFL Scores for 2003 Week #14
==============================
In an exciting week the playoff race came down to the Monday night 
game.  

The key game being the Illuminati's 34-5 victory over the Werewolves.
This clinched the #3 seed for the Illuminati.  They will play the 
Crusaders next week.  With the Werewolves losing, the door was open
for the MeggaMen to grab the final playoff spot.  But a 0-0 tie to 
the Green Wave, their season comes to an end.  The tie by the Green
Wave left both the final playoff spot and the Toliet Bowl berth up 
to the results of the Freezer Burn vs Whiskey Tango game. 

In that game the Freezer Burn defeated the Whiskey Tango 71-4.  This
sends the Whiskey Tango to the Toliet Bowl against the Norsemen.  It
also puts the Freezer Burn into the final playoff spot, playing the 
War Eagles next week.  With the Freezer Burn winning, the Crusaders 
45-39 win over the Rednecks doesn't effect the playoff standings, but
it is the 8th straight time the Crusaders have defeated the Rednecks.
In the final game of the week the War Eagles defeat the Norsemen 59-35.

                                    
