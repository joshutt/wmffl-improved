<P>As we did the past couple of years I am once again soliciting nominations for
the three voted on ownership awards; The Bonehead Award, Best Ownership Move 
and Overall Best Owner.  The Bonehead Award goes to the team that made the 
stupidest move or moves that hurt that team's own chances of success.  The 
Best Ownership Move is awarded to the single best move that improved a team's
success.  Finally, the Overall Best Owner goes to the person that performed the
best in all aspects of the game, for this award everything can and should be 
considered including, what the team started with, how the team finished, the
quality of drafting and transactions, etc.</P>

<P>I ask that everyone consider which owners they think should awarded each of 
these awards.  Send me an email with you nominations and reasons for the
nomination.  Next week the voting will take place.</P>

<P>Josh</P>

