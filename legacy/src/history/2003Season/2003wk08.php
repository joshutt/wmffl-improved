WMFFL Scores for 2003 Week #8
=============================
Whiskey Tango activated a bye week player, so they get a 2 point 
penalty and a $1 fine.

The Rednecks suffer their first loss of the season to the Crusaders, 
37-33.  This ends a 10 game unbeaten streak, the third longest in 
league history.  It is also the 7th straight time the Crusaders have 
defeated the Rednecks.  The War Eagles, however, remained unbeaten 
with a 13-0 victory over the MeggaMen.  Their unbeaten streak is now 
8 games.  In a stunning turn of events, the Green Wave surged back on
Monday night to force a 0-0 tie with the Whiskey Tango.  This prevents
the Green Wave from setting the record for consecutive losses, but 
does set the record for consecutive winless games, 13.  In other 
games, the Werewolves defeated the Freezer Burn 34-8 and the Norsemen
kept the playoff race close by beating the Illuminati 46-22.

                                  
