WMFFL Newsletter Week #13
Home Page: http://wmffl.com
=======================================================


The Illuminati have clinched the Blue Division.  

The Illuminati activated Jason Ferguson (DL-NYJ) whom they cut on 
November 18th.  This is an illegal activation and results in a -2 
point penalty and a $1 fine.  Ferguson's score, is of course set to 
0.  The Norsemen also activated Jose Cortez (K-None) who was cut from
San Francisco last Tuesday.  As a result he was not on a team when 
activations where due on Thursday, which is an illegal activation a 2
point penalty and a $1 fine.

Single Week Records (Week 12)
  - Priest Holmes is offically a freak.  He scored 45 points in week 
  12, the most ever scored in one week by a RB, breaking Marshall 
  Faulk's record of 44 set in week 7.  This is also the 2nd best 
  point total by any position.  Faulk only held the RB record for 5 
  weeks, Holmes will only hold it for a week (take a look at the box 
  scores to see what I mean).

Single Season Records (through week 13)
   -  Priest Holmes (RB-KC) has broken the single season record for 
   points by a RB.  The old record was set in 1995 by Emmitt Smith 
   and was 262.  Holmes currently has 270.  The most points scored by
   any player at any position in a season is 287, by Steve Young in 
   1994, but it was as part of the San Francisco QB unit which scored
   a total of 299 that season. 
   -  Derrick Brooks (LB-TB) 122 moves into #6 of LBs, 1 behind London
   Flecther's 2001 season, and 37 away from the record of 199.
   -  Andy Reid (HC-PHI) has scored 40 points this season making him 
   the 8th highest scoring HC.  He is 1 point behind his last year 
   total for 7th and 2 behind the 6th place mark.  The record is 51.
   -  John Gruden (HC-TB) is tied for the 10th place single season HC
   record with 37 points.  Another win will jump him over Geroge 
   Seyfirt's 1994 season of 39.
   -  The Kansas City Offensive Line has ridden Priest Holmes to 124 
   points this year, 4th best by an OL unit.  They trail the 1998 
   Denver unit by 22 points and are 27 points from the 2001 Steelers 
   for the record.


