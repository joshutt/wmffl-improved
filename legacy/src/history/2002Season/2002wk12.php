WMFFL Newsletter Week #12
Home Page: http://wmffl.com
=======================================================
If you followed along with the online scoring for week 12, there are 
a couple of surprises.  Most notably, some of the QBs were 
incorrectly scored in the online version.  So two games had different
results and a third one almost did.  So much for automated scoring. 

The Illuminati activated Jason Ferguson (DL-NYJ) whom they cut on 
November 18th.  This is an illegal activation and results in a -2 
point penalty and a $1 fine.  Ferguson's score, is of course set to 
0.

