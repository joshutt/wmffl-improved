<P>In addition to the draft order, the schedule is partially based on
order of finish.  Every team plays every other team in their division
2 times and every team in the opposing division once.  However, that only
leaves 13 games.  The remaining game comes from playing one team in
the opposing division a second time.  The teams that finished in the same
relative position the year before meet in this extra game.  Below are the
extra game matchups for next season.</P>

