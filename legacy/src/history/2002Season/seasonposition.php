<P>
This year there is a change in how draft order will be determined.  Previously,
all teams received a weight based on a number of factors, mainly how they did
the previous season.  The new system is reverse order of finish, with a
lottery among the non-playoff teams for the first three slots.  The weights
for the lottery will be based on the order of finish.  The draft order will
remain the same throughout the draft.  Shown below is a
table of every team's possible draft positions and lottery weight.</P>

