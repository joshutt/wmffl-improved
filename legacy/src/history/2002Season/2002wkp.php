WMFFL Newsletter Playoffs 
Home Page: http://wmffl.com
=======================================================
In the Toliet Bowl the Rednecks prove victorious over the War Eagles 
by a 38-15 margin.  The Werewolves defeated the Illuminati by a score 
of 19-6.  This is the Illuminati's 4th straight playoff loss.  The 
MeggaMen cameback against the Norsemen to win 60-56.  This will be 
their first trip to the Championship game.  As a reminder to the 
Werewolves and MeggaMen, there are Saturday games this week, so 
activations are due at Noon on Saturday.


