<P>
In order to alleviate the fear of rigging the draft order selection,
this year I have selected a third party service, called "Irony Games:
Dice Server", to select our draft order.  You can get information about
this service at <A HREF="http://irony.com/mailroll.html">
http://irony.com/mailroll.html</A>.  </P>

<P>
Basiclly, the way it will work is this.  Every team involved in the
lottery willrecieve a set of numbers based on their draft weight (ie,
Rednecks will get 1 through 6; MeggaMen 7 through 11; etc) (see chart
below).  This will leave 21 numbers assigned.  Since the smallest die
size offered that is at least 21 is 25, the number 22 through 25 will be
ignored.  When I do the roll everyone will recieve an email from the
secure dice server that will list the results of 20 rolls.  (I am
rolling 20 figuring that some will need to be ignored).  This email will
be a PGP signed message which you can verify yourself if you have PGP or
may go to the verification page provided by irony:
<A HREF="http://irony.com/verifyroll.html">
http://irony.com/verifyroll.html</A> (I have included the Irony
Public Key below for those interested, it is also available on the
verification page) to ensure that the rolls actually came from Irony.
</P>

<P>
Whoever, has the number the corresponds to the first number rolled will
recieve the first pick.  If this number is an ignored number or a number
belonging to a team who has already had a pick assigned I will move to
the next number on the list.  The same will be done for picks 2 and 3.
(Remember only the first three picks are lottery based, picks 4-6 will
be filled with the remaining lottery teams.)  Any remaining numbers will
be disgarded.  I have included an example at the bottom of the page.  I
intend to do the drawing on Friday, so if there are any questions please
ask them now.  Thanks.</P>

<P>Josh</P>

