WMFFL Newsletter Week #4
Home Page: http://wmffl.com
=======================================================
While the Barbarians did finally get a full lineup in this week,  
it turns out that the Kicker they picked up was also on a bye.  
So they still get a 2 point and $1 penalty.

Only one other time in league history have there been two 4-0 teams
in the same year.  There have never been two 5-0 teams in the same 
year.  (In 1998 the Norsemen and the Werewolves were both 4-0, in 
week 5 the Norsemen beat the Werewolves, 68-0)  With wins next week
the Illuminati and the MeggaMen will meet in week 6, both at 5-0.

The same is true for 0-4 teams.  In 1999 the Green Wave and the 
Archers Who Say Ni were both 0-4.  The Green Wave beat the 
Barbarians in week 5 and the Archers Who Say Ni lost to the 
Norsemen.  The Crusaders and Rednecks will play this upcoming week, 
both are 0-4.  

