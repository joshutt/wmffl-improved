WMFFL Newsletter Week #9
Home Page: http://wmffl.com
=======================================================

This week for the first time, we had a situation where
two teams playing each other had negative scores.  As
we planned for this in the rules, the team with the
higher score gets a score of 1 while the other team
gets 0. (Example, one team scores -5 and another get
-7, the -5 team will win 1-0).  In this case that means
that the Archers Who Say Ni have defeated the Barbarians
1-0.  This is the score that gets reflected in the
standings.  In the box scores you will notice that
there is a new line item titled "Team (Bonus) Points"
It should be 0 for everyone except for Mike, who will
have the necessary points for his score to get to 1.
If there are any questions about this rule let me know.
Thanks.

Also, this is a reminder that the transaction deadline
is quickly approaching.  So if you want to make transactions
you have 2 weeks remaining.

Josh


