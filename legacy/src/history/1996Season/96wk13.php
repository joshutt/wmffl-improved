Notes From the Commissioner
===============================
REMINDER
  Activations for week 14 must be turned in before the ThanksGiving
Day games.

Playoff Picture
Norsemen - Have Clinched PLayoff Berth
           Can win division with win vs. Renegades

Renegades - Have Clinched Playoff Berth
            Can win division with win or tie vs. Norsemen

Barbarians - Have Clinched Playoff Berth
             Can clinch Division with a win or a tie or an Iradicators
             loss or tie.

Iradicators - Can win Division with a win and a Barbarian lose.
             Can clinch playoff berth with win vs. Warriors

Warriors - Can clinch playoff berth with win vs. Iradicators or
           tie with Iradicators and a tie between Slayers and Tsunami.

Slayers - Can clinch playoff berth with win vs. Tsunami and tie
   between Warriors and Iradicators.

Tsunami - Can clinch playoff berth with win vs. Slayers and tie
   between Warriors and iradicators.

Fighting Squirrels - Not in playoff contention.

Archers Who Say Ni - Not in playoff contention.


Week 12 Record Breakers
 - The Washington Kicking Unit of the Fighting Squirrels tied the
kicker record with 4 Field Goals in the 39 yards and less range.

 - Chris Doleman of the Fighting Squirrels broke the record for points
by a Defensive Lineman.  He scored 22, the old record was 17.

 - The record for best record by a team will be broken this week.
Whoever wins the Renegades vs. Norsemen game will have the best
record in league history.  If they tie then they will both have it.
The old record was set by the Barbarians in 1994 and was 12-2 .857


